    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        sql_login modsql = new sql_login();
        String pass = new String(txtPassword.getPassword());
        if (!txtUsuario.getText().equals("") && !pass.equals("")) {
            log.setUsuario(txtUsuario.getText());
            log.setContrasenia(pass);
            if (modsql.login(log)) {
                if (log.getStatus() == 1) {
                    //home = null;
                    if (home == null) {
                        this.dispose();
                        //home formHome = new home(log);
                        home = new home(log);
                        home.setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Usuario Inactivo");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Datos incorrectos");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Llenar los campos vacios");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

