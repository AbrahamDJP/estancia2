    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (!lblidVenta.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(this, "Confirmar la eliminacion de la venta? \n ");
            if (confirmacion == 0) {
                String idVenta = lblidVenta.getText();
                int idVnta = Integer.parseInt(idVenta);
                ventas.setId_venta(idVnta);
                if(sql_vetas.eliminar(ventas)){
                    sql_vetas.cargar(this);
                    limpiar();
                }
            } else if (confirmacion == 1) {
            } else {
                limpiar();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona la venta para editar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

