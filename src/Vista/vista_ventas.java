    private void btnAgregarVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarVActionPerformed
        // TODO add your handling code here:
        if (!lblRol.getText().equals("") && !lblEmpleado.getText().equals("")
                && cbbxProducto.getSelectedIndex() != 0 && !txtUnidades.getText().equals("")
                && cbbxCliente.getSelectedIndex() != 0 /*&& !txtIva.getText().equals("")*/) {

            String codProd = cbbxProducto.getItemAt(cbbxProducto.getSelectedIndex());
            String cantProd = txtUnidades.getText();
            String usuario = lblEmpleado.getText();
            String cliente = cbbxCliente.getItemAt(cbbxCliente.getSelectedIndex());
            String precio = lblPrecio.getText();
            //String impuestoTxt = txtIva.getText();

            int idProd = sql_vetas.obtenerIdProd(codProd);
            int cantidadProd = Integer.parseInt(cantProd);
            int idUsr = sql_vetas.obtenerIdUsuario(usuario);
            int idCliente = Integer.parseInt(cliente);
            double precioProd = Double.parseDouble(precio);
            double total = cantidadProd * precioProd;
            //int impuesto = Integer.parseInt(impuestoTxt);
            //double xctajeImpuesto = cantNeto / 100;
            //double total = xctajeImpuesto + cantNeto;
            System.out.println(idProd);
            System.out.println(cantidadProd);
            System.out.println(idUsr);
            System.out.println(cliente);
            System.out.println(precioProd);
            //System.out.println(cantNeto);
            System.out.println(total);
            int confirmacion = JOptionPane.showConfirmDialog(this, "Confirmar Compra \n "
                    + "Producto: " + codProd + "\n"
                    + "Precio Total: " + total);
            if (confirmacion == 0) {
                ventas.setFk_producto(idProd);
                ventas.setCantidad_producto_vendido(cantidadProd);
                ventas.setFk_usuario(idUsr);
                ventas.setFk_cliente(idCliente);
                //ventas.setCantidad_neto(cantNeto);
                //ventas.setImpuesto_venta(impuesto);
                ventas.setCantidad_total(total);
                //int stock = sql_vetas.stockDisponible(codProd);
                if (sql_vetas.stockDisponible(ventas) >= cantidadProd) {
                    if (sql_vetas.registrar(ventas) && sql_vetas.actualizarStockProd(ventas)) {
                        JOptionPane.showMessageDialog(this, "Venta Realizada");
                        limpiar();
                        if (sql_vetas.cargar(this)) {
                        }
                        
                    } else {
                        JOptionPane.showMessageDialog(this, "Error al agregar venta");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "No hay suficientes unidades");
                }
            } else if (confirmacion == 1) {
            } else {
                limpiar();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Llena los campos correctamente");
        }
    }//GEN-LAST:event_btnAgregarVActionPerformed

